# Conditional Statements

"""If Else Statement"""
a = int(input("Enter a number : "))

if a > 10:
    print(f"{a} is greater than 10")
else:
    print(f"{a} is smaller than 10")


"""If Elif Else Statement"""
money = int(input("Enter the amount of money : "))

if money == 10:
    print("Chocobar")
elif money == 20:
    print("Big Chocobar")
else:
    print("Cone")
